package model.bean;

public class YeuCau {
	private String maYeuCau;
	private String tenSach;
	private String maNguoiYeuCau;

	public String getMaYeuCau() {
		return maYeuCau;
	}

	public void setMaYeuCau(String maYeuCau) {
		this.maYeuCau = maYeuCau;
	}

	public String getTenSach() {
		return tenSach;
	}

	public void setTenSach(String tenSach) {
		this.tenSach = tenSach;
	}

	public String getMaNguoiYeuCau() {
		return maNguoiYeuCau;
	}

	public void setMaNguoiYeuCau(String maNguoiYeuCau) {
		this.maNguoiYeuCau = maNguoiYeuCau;
	}

}
